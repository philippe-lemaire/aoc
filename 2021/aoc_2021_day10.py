def remove_corrupt_lines(data):
    new_data = []
    for line in data:
        if not find_illegal_char(line):
            new_data.append(line)
    return new_data

def find_ending(line):
    openings =[]
    closings = []
    openers = ['(', '[', '{', '<']
    closers = [')', ']', '}', '>']
    
    for index, char in enumerate(line):
        if char in openers:
            openings.append(char) # store openings as their are seen
        if char in closers:
            if closers.index(char) == openers.index(openings[-1]): # signs match
                openings.pop() # remove last seen and matched opening
 
    # just need to reverse openings and list the matchings closings

    match = dict(zip(openers, closers))
    new_closings = [match.get(op) for op in reversed(openings)]
    
    return new_closings

